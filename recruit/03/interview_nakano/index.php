          <h2>『患者様のQOLを実現するための窓口に』</h2>
          <p>
            私が所属している地域連携室は、クリニックの窓口として新規患者様の受け入れ調整を行っています。<br>常に患者様を第一に考えながら、医師が最善の状況で診療できるよう環境を整えることが私たちの仕事です。<br><br>
            当院では「真の社会貢献」を目指し、地域医療の充実に向けた地域包括システムを構築しています。<br>地域包括システムにおいて、決して医療が一番上に位置するわけではありません。<br>患者様を中心とし、地域の病院や施設、ケアマネージャー、訪問看護師、保健所、地域包括センターなどがネットワークを組み、互いに支え合いながら、患者様にとって快適なQOLを実現したいと考えています。<br><br>
            従来のやり方に捉われることなくお互いに刺激し合い、ふたば在宅クリニックならではのやり方で地域を活性化していければと思います。
          </p>
