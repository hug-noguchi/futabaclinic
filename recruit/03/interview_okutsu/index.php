          <h2>『レセプト業務はクリニックの経営を支える大事な仕事』</h2>
          <p>
            新卒では総合病院に入社しました。忙しいながらもたくさんのことを学び、充実した日々を送っていました。<br>その後、新たなことに挑戦しようと、以前から興味のあった電子カルテのインストラクターへ転身したり、消化器内科・循環器内科クリニックで働いたりと、医療業界での経験を積んできました。<br><br>
            しかし、仕事にやりがいを感じる一方、どの職場も夜遅くまでの勤務が多く、家庭との両立を難しく感じていました。<br>そこで、ふたば在宅クリニックの訪問診療を見かけ、興味を持ちました。<br><br>
            訪問診療は全く知識がなく未知の世界でした。<br>しかし、訪問診療は患者様の診療予定が決まっているため、比較的勤務時間が定まっています。<br>なにより、訪問診療の世界をもっと知りたいと思い、働くことを決めました。<br><br>
            現在は、主にレセプト業務を担当しています。<br>医師や看護師が患者様を伺ったのち、診療内容を患者様に請求したり、社会保険や国民健康保険の計算をしたりする仕事です。<br>点数の計算はクリニックの経営にも関わる大事な部分であるため、分からないことは一つひとつ調べながら、漏れのない正確な仕事を心掛けています。
          </p>
