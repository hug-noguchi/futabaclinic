            特に、保険に関わる知識については、ふたば在宅クリニックで働き始めてから初めて知ることが多くありました。<br>難しいながらも関心を持って勉強し、日々成長を目指しています。<br><br>
            また、診療を行う医師や看護師と近い場所で働けるので、「こんな病状だからこういう処置をする」ということを学ぶ機会を得られます。<br>今まで“点”でしか理解していなかったことが“線”でつながっていくような感覚があります。
