            「人生の中で、自分の仕事に意味が持てるか。僕は持てる。」<br>
            「訪問診療という場で、直接患者様から感謝をいただける、時によっては、その方の人生の集大成といえる時に言葉をいただける。訪問診療という仕事の価値はそこにある。」<br>
            というお話でした。
            <br><br>
            改めて自分の今後を考えたときに、在宅医療に携わることで、一層これからの人生で価値を生み出していけるかもしれないと感じました。<br>
