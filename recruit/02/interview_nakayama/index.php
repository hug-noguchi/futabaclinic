          <h2>『医療事務の経験を活かしながら総務に挑戦』</h2>
          <p>
            私はふたば在宅クリニックの総務として働いています。<br>
            業務は電話受付やクリニック内の環境整備、人事調整、患者様の診療費管理など多岐にわたり、事務的なものからスタッフのサポートまで対応しています。<br><br>
            以前は病院やクリニックで医療事務をしていたのですが、もともと総務という職種に興味がありました。<br>
            前職を退職し、総務の仕事を探していた際に出会ったのがふたば在宅クリニックです。<br>
            在宅医療というものを初めて知り興味が湧いたこと、そしてこれまでの医療事務の経験を活かしながら総務に挑戦できることに惹かれ、入社しました。<br><br>
            これまでと同じ医療業界であるとはいえ、総務は完全に未経験ですので、初めてのことばかりで勉強しがいのある毎日です。<br>
            自分で分からないことはすぐに調べながら知識を身に付け、患者様に快適な在宅医療を提供できるよう日々努力しています。
          </p>
