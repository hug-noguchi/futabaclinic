<script type="text/javascript">
  $(document).ready(function() {
    $('body').fadeIn(1500);
    $(window).on("beforeunload", function(e) {
      if (e.target.activeElement.tagName === 'A' && e.target.activeElement.href.startsWith('mailto:')) {
        return; // フェードアウトをスキップ
      }
      $('body').fadeOut(1500);
    });
  });

  $(window).on('load' , function() {
    $("body").removeClass("preload");
  });

  $(function() {
    // 各院トップページタブ
    $('.news_list .tab li').on('click touchend', function(e) {
    e.preventDefault(); // スマホでの意図しない挙動を防ぐため
    var index = $('.news_list .tab li').index(this);

    $('.news_list .tab li').removeClass('active');
    $(this).addClass('active');

    $('.news_show .show').removeClass('active').eq(index).addClass('active');
    });

    var nav = $('.nav');
    //navの位置
    var navTop = nav.offset().top;
    //スクロールするたびに実行
    $(window).scroll(function () {
      var winTop = $(this).scrollTop();
      //スクロール位置がnavの位置より下だったらクラスfixedを追加
      if (winTop >= navTop) {
        nav.addClass('fixed')
      } else if (winTop <= navTop) {
        nav.removeClass('fixed')
      }
    });

    var topBtn = $('#page-top');
    topBtn.hide();
    //スクロールが100に達したらボタン表示
    $(window).scroll(function () {
      if ($(this).scrollTop() > 100) {
        topBtn.fadeIn();
      } else {
        topBtn.fadeOut();
      }
    });
    //スクロールしてトップ
    topBtn.click(function () {
      $('body,html,').animate({
        scrollTop: 0
      }, 500);
      return false;
    });

    $('a[href^=#]').click(function(){
      var speed = 500;
      var href= $(this).attr("href");
      var target = $(href == "#" || href == "" ? 'html' : href);
      var position = target.offset().top;
      $("html, body").animate({scrollTop:position}, speed, "swing");
      return false;
    });

    $(document).ready(function() {
      $('.drawer').drawer();
    });

    var fixed = $('.fixed__cv');
    fixed.hide();

    $(window).scroll(function () {
      if ($(this).scrollTop() > 100) {
        fixed.fadeIn();
      } else {
        fixed.fadeOut();
      }
    });
  });
</script>
